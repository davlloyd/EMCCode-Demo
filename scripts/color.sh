#!/usr/bin/env bash

LOG_OUTPUT="/tmp/rackhd-demo-setup.log"

# Added this function to make the individual actions prompts readable
logger() {
  LOG_TYPE=$1
  MSG=$2

  COLOR_OFF="\x1b[39;49;00m"
  case "${LOG_TYPE}" in
      green)
          # Green
          COLOR_ON="\x1b[32;01m";;
      blue)
          # Blue
          COLOR_ON="\x1b[36;01m";;
      yellow)
          # Yellow
          COLOR_ON="\x1b[33;01m";;
      red)
          # Red
          COLOR_ON="\x1b[31;01m";;
      default)
          # Default
          COLOR_ON="${COLOR_OFF}";;
      *)
          # Default
          COLOR_ON="${COLOR_OFF}";;
  esac

  TIME=$(date +%F" "%H:%M:%S)
  echo "${COLOR_ON} ${TIME} -- ${MSG} ${COLOR_OFF}"
  echo "${TIME} -- ${MSG}" >> "${LOG_OUTPUT}"
}

logger "red" "Logger loaded"
